package Robot;
/**
 * The Class Response.
 * 
 * @author GUO
 * 
 */
public class Response {


    /** 属性:code. */
    private int code;

    /** 属性:content. */
    private String content;

    /**
     * 获取对象中的的code.
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * 设置对象中的code.
     * @param new code
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * 获取对象中的content.
     * @return the content
     */
    public String getContent() {
        return content;
    }

    /**
     * 设置对象中的content.
     * @param content the new content
     */
    public void setContent(String content) {
        this.content = content;
    }
}


